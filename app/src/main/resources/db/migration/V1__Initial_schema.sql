/* =========================================================
   2. Venues & Structure
   ========================================================= */

-- The Main Venue Entity
create table venues
(
    id                  uuid                        not null primary key,
    organization_id     uuid                        not null, -- Parent Organization
    city_id             bigint                      not null
        constraint fk_venue_city references ref_cities (id),
    merchant_profile_id uuid,
    category_id         bigint
        constraint fk_venue_category references ref_venue_categories (id),

    -- Content
    name                varchar(255)                not null,
    slug                varchar(255)                not null unique,
    description         text,
    address             varchar(500)                not null,

    -- Legal & Config
    legal_name          varchar(255),
    tax_id              varchar(50),
    time_zone           varchar(50)                 not null default 'Asia/Yerevan',
    latitude            double precision,
    longitude           double precision,
    is_always_open      boolean                     not null default false,
    custom_domain       varchar(255),
    ownership_type      varchar(20) check (ownership_type in ('STATE_OWNED', 'MUNICIPAL', 'PRIVATE', 'NGO', 'OTHER')),
    status              varchar(20)                 not null default 'PENDING_APPROVAL'
        check (status in ('PENDING_APPROVAL', 'ACTIVE', 'SUSPENDED', 'INACTIVE', 'DELETED')),

    -- Contact & Visuals
    phone_number        varchar(50),
    website             varchar(500),
    contact_email       varchar(255),
    logo_url            varchar(500),
    cover_image_url     varchar(500),
    social_links        jsonb,
    notification_emails jsonb                       not null default '[]'::jsonb,

    created_at          timestamp(6) with time zone not null,
    last_modified_at    timestamp(6) with time zone not null
);
create index idx_venue_city on venues (city_id);
create index idx_venue_slug on venues (slug);
create index idx_venue_status on venues (status);

-- Organization-Venue Link (Junction Table)
create table organization_venue_links
(
    id               bigint generated by default as identity primary key,
    organization_id  uuid                        not null
        constraint fk_org_venue_link_org references organizations (id),
    venue_id         uuid                        not null
        constraint fk_org_venue_link_venue references venues (id),
    is_active        boolean                     not null default true,
    created_at       timestamp(6) with time zone not null,
    last_modified_at timestamp(6) with time zone not null
);
create index idx_org_venue_link_org on organization_venue_links (organization_id);
create index idx_org_venue_link_venue on organization_venue_links (venue_id);
create index idx_org_venue_link_active on organization_venue_links (is_active);

-- Venue Sensitive Settings (Vertical Partitioning)
create table venue_settings
(
    venue_id           uuid                        not null primary key
        constraint fk_venue_settings_venue references venues (id) on delete cascade,
    smtp_config_json   text,
    custom_config_json text,
    created_at         timestamp(6) with time zone not null default now(),
    last_modified_at   timestamp(6) with time zone not null default now()
);

create table venue_branding
(
    venue_id        uuid not null primary key
        constraint fk_venue_branding_venue references venues (id) on delete cascade,
    primary_color   varchar(7),
    secondary_color varchar(7),
    favicon_url     varchar(500),
    home_hero       jsonb,
    about_blocks    jsonb,
    contact_config  jsonb
);

