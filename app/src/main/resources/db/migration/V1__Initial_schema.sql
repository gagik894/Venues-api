/* =========================================================
   1. Reference Data & Independent Tables
   ========================================================= */

-- Location hierarchy
create table ref_regions
(
    id               bigint generated by default as identity primary key,
    code             varchar(10)                 not null unique,
    names            jsonb                       not null,
    display_order    integer,
    is_active        boolean                     not null default true,
    created_at       timestamp(6) with time zone not null,
    last_modified_at timestamp(6) with time zone not null
);
create index idx_region_code on ref_regions (code);

create table ref_cities
(
    id               bigint generated by default as identity primary key,
    region_id        bigint                      not null
        constraint fk_city_region references ref_regions (id),
    slug             varchar(100)                not null unique,
    names            jsonb                       not null,
    official_id      varchar(50),
    display_order    integer,
    is_active        boolean                     not null default true,
    created_at       timestamp(6) with time zone not null,
    last_modified_at timestamp(6) with time zone not null
